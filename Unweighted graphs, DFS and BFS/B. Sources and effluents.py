# –í—Ö–æ–¥–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ
# –°–Ω–∞—á–∞–ª–∞ –≤–≤–æ–¥–∏—Ç—Å—è —á–∏—Å–ª–æ n ( 1‚â§ùëõ‚â§100
