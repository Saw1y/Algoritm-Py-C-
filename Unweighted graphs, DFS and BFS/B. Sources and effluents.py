# –í—ã—Ö–æ–¥–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ
# –í–Ω–∞—á–∞–ª–µ –≤—ã–≤–µ–¥–∏—Ç–µ k ‚Äì —á–∏—Å–ª–æ –∏—Å—Ç–æ–∫–æ–≤ –≤ –≥—Ä–∞—Ñ–µ –∏ –∑–∞—Ç–µ–º k —á–∏—Å–µ–ª ‚Äì –Ω–æ–º–µ—Ä–∞ –≤–µ—Ä—à–∏–Ω, –∫–æ—Ç–æ—Ä—ã–µ —è–≤–ª—è—é—Ç—Å—è –∏—Å—Ç–æ–∫–∞–º–∏, –≤ –≤–æ–∑—Ä–∞—Å—Ç–∞—é—â–µ–º –ø–æ—Ä—è–¥–∫–µ. –ó–∞—Ç–µ–º –≤—ã–≤–µ–¥–∏—Ç–µ –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ —Å—Ç–æ–∫–∞—Ö –≤ —Ç–æ–º –∂–µ –ø–æ—Ä—è–¥–∫–µ.

n = int(input())
a = [[0 for _ in range(n)] for _ in range(n)]
for i in range(n):
    edge = list(map(int, input().split()))
    for j in range(n):
        a[i][j] = edge[j]

# c—Ç–æ–∫–∏
sinks = []
for i in range(n):
    if sum(a[i]) == 0:
        sinks.append(i + 1)

# –∏—Å—Ç–æ–∫–∏
sources = []
for i in range(n):
    sm = 0
    for j in range(n):
        sm += a[j][i]
    if sm == 0:
        sources.append(i + 1)

print(len(sources))
for i in range(len(sources)):
    print(sources[i])
print(len(sinks))
for i in range(len(sinks)):
    print(sinks[i])
