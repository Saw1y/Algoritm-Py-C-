# –í—ã—Ö–æ–¥–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ
# –í—ã–≤–µ–¥–∏—Ç–µ  ¬´YES¬ª, –µ—Å–ª–∏ –≥—Ä–∞—Ñ —è–≤–ª—è–µ—Ç—Å—è –ø–æ–ª–Ω—ã–º, –∏ ¬´NO¬ª –≤ –ø—Ä–æ—Ç–∏–≤–Ω–æ–º —Å–ª—É—á–∞–µ.

n, m = map(int, input().split())
edges = set()
for i in range(m):
    vertex, edge = map(int, input().split())
    if vertex == edge:
        continue
    edges.add((min(vertex, edge), max(edge, vertex)))

if len(edges) == n * (n - 1) // 2:
    print("YES")
else:
    print("NO")
