// Когда Петя учился в школе, он часто участвовал в олимпиадах по информатике, математике и физике.
// Так как он был достаточно способным мальчиком и усердно учился, то на многих из этих олимпиад он получал дипломы. К окончанию школы у него накопилось n дипломов, причём, как оказалось,
// все они имели одинаковые размеры: w — в ширину и h — в высоту. Сейчас Петя учится в одном из лучших российских университетов и живёт в общежитии со своими одногруппниками.
// Он решил украсить свою комнату, повесив на одну из стен свои дипломы за школьные олимпиады. Так как к бетонной стене прикрепить дипломы достаточно трудно, то он решил купить специальную доску из пробкового дерева, чтобы прикрепить её к стене, а к ней — дипломы. Для того чтобы эта конструкция выглядела более красиво, Петя хочет, чтобы доска была квадратной и занимала как можно меньше места на стене. Каждый диплом должен быть размещён строго в прямоугольнике размером w
// на h. Дипломы запрещается поворачивать на 90 градусов. Прямоугольники, соответствующие различным дипломам, не должны иметь общих внутренних точек.
// Требуется написать программу, которая вычислит минимальный размер стороны доски, которая потребуется Пете для размещения всех своих дипломов.

// Входные данные
// Входной файл содержит три целых числа: w, h, n
//  (1≤w,h,n≤109).

// Выходные данные
// В выходной файл необходимо вывести ответ на поставленную задачу.

#include <iostream>
#include <algorithm>

using namespace std;

bool good(long long x, long long w, long long h, long long n){
    bool res = ((x / w) * (x / h)) >= n;
    return res;
}


int main() {
    long long w, h, n;
    cin >> w >> h >> n;
    long long left = 0;
    long long right = h >= w ? n * h : n * w;
    long long m;
    while (right - left > 1)
    {
        m = (left + right) / 2;
        if (good(m, w, h, n))
        {
            right = m;
        }
        else
        {
            left = m;
        }
    }
    cout << right;
    return 0;
}
