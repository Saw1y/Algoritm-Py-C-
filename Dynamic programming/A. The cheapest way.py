# # –ú–∞–ª—å—á–∏–∫ —É–º–µ–µ—Ç –ø–µ—Ä–µ—à–∞–≥–∏–≤–∞—Ç—å –Ω–∞ —Å–ª–µ–¥—É—é—â—É—é —Å—Ç—É–ø–µ–Ω—å–∫—É, –ª–∏–±–æ –ø–µ—Ä–µ–ø—Ä—ã–≥–∏–≤–∞—Ç—å —á–µ—Ä–µ–∑ —Å—Ç—É–ø–µ–Ω—å–∫—É. 
# # –¢—Ä–µ–±—É–µ—Ç—Å—è —É–∑–Ω–∞—Ç—å, –∫–∞–∫–∞—è –Ω–∞–∏–º–µ–Ω—å—à–∞—è —Å—É–º–º–∞ –ø–æ–Ω–∞–¥–æ–±–∏—Ç—Å—è –º–∞–ª—å—á–∏–∫—É, —á—Ç–æ–±—ã –¥–æ–±—Ä–∞—Ç—å—Å—è –¥–æ –≤–µ—Ä—Ö–Ω–µ–π —Å—Ç—É–ø–µ–Ω—å–∫–∏.

# –í—Ö–æ–¥–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ
# –í –ø–µ—Ä–≤–æ–π —Å—Ç—Ä–æ–∫–µ –≤—Ö–æ–¥–Ω–æ–≥–æ —Ñ–∞–π–ª–∞ –≤–≤–æ–¥–∏—Ç—Å—è –æ–¥–Ω–æ –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ ùëÅ‚â§100
#  ‚Äî –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç—É–ø–µ–Ω–µ–∫.
# –í —Å–ª–µ–¥—É—é—â–µ–π —Å—Ç—Ä–æ–∫–µ –≤–≤–æ–¥—è—Ç—Å—è ùëÅ
#  –Ω–∞—Ç—É—Ä–∞–ª—å–Ω—ã—Ö —á–∏—Å–µ–ª, –Ω–µ –ø—Ä–µ–≤–æ—Å—Ö–æ–¥—è—â–∏—Ö 100 ‚Äî —Å—Ç–æ–∏–º–æ—Å—Ç—å –∫–∞–∂–¥–æ–π —Å—Ç—É–ø–µ–Ω—å–∫–∏ (—Å–Ω–∏–∑—É –≤–≤–µ—Ä—Ö).

# –í—ã—Ö–æ–¥–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ
# –í—ã–≤–µ–¥–∏—Ç–µ –æ–¥–Ω–æ —á–∏—Å–ª–æ ‚Äî –Ω–∞–∏–º–µ–Ω—å—à—É—é –≤–æ–∑–º–æ–∂–Ω—É—é —Å—Ç–æ–∏–º–æ—Å—Ç—å –ø—Ä–æ—Ö–æ–¥–∞ –ø–æ –ª–µ—Å–µ–Ω–∫–µ.

n = int(input())
arr = list(map(int, input().split()))
dp = [0] * (n + 1)

for i in range(1, n + 1):
    if i == 1:
        dp[i] = arr[0]
    else:
        dp[i] = arr[i - 1] + min(dp[i -1], dp[i - 2])
print(dp[-1])
